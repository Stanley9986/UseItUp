export function translate(languageCode: SupportedLanguageCode, key: TranslationKey, params?: TranslationParams) {
  return interpolate(dictionaries[languageCode]?.[key] ?? en[key], params);
}

function interpolate(value: string, params?: TranslationParams) {
  if (!params) {
    return value;
  }

  return Object.entries(params).reduce(
    (text, [key, replacement]) => text.replaceAll(`{${key}}`, String(replacement)),
    value,
  );
}
